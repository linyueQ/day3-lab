// 生成基金净值趋势数据
export const generateNavData = (fundCode, period = "3m") => {
  const periods = {
    "1m": 30,
    "3m": 90,
    "6m": 180,
    "1y": 365
  };
  
  const days = periods[period] || 90;
  const baseNav = 1.5;
  const volatility = 0.02;
  
  const navData = [];
  let currentNav = baseNav;
  const endDate = new Date("2026-04-14");
  
  for (let i = days; i >= 0; i--) {
    const date = new Date(endDate);
    date.setDate(date.getDate() - i);
    
    const change = (Math.random() - 0.48) * volatility;
    currentNav = currentNav * (1 + change);
    
    navData.push({
      date: date.toISOString().split("T")[0],
      accumulatedNav: parseFloat(currentNav.toFixed(4)),
      dailyGrowth: parseFloat((change * 100).toFixed(2))
    });
  }
  
  return navData;
};

// 获取局势分值颜色
export const getScoreColor = (score) => {
  if (score <= 20) return "#ef4444"; // 红色 - 高度紧张
  if (score <= 50) return "#f97316"; // 橙色 - 中等紧张
  return "#10b981"; // 绿色 - 趋于缓和
};

